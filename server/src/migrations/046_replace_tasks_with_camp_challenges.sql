-- 3) Bind every task to every sector of the matching difficulty so freshly
-- generated maps have a full task pool per sector.
INSERT INTO sector_tasks (sector_id, task_id)
SELECT s.id, t.id
  FROM sectors s
  JOIN tasks t ON t.difficulty_id = s.difficulty_id
ON CONFLICT (sector_id, task_id) DO NOTHING;
