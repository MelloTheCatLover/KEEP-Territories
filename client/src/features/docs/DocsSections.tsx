function GoodsList({
  items,
  icon,
  placeholder,
}: {
  items: Array<[string, string]>;
  icon: React.ReactNode;
  placeholder?: string;
}) {
  if (items.length === 0 && placeholder) {
    return <p className="text-sm text-neutral-700">{placeholder}</p>;
  }
  return (
    <ul className="space-y-2">
      {items.map(([name, desc]) => (
        <li key={name} className="flex gap-2.5 items-start">
          <span className="mt-0.5 flex-shrink-0">{icon}</span>
          <span className="text-sm">
            <b className="text-neutral-1000">{name}</b>{' '}
            <span className="text-neutral-700">— {desc}</span>
          </span>
        </li>
      ))}
    </ul>
  );
}

